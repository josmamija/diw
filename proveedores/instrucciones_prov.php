  <p><strong>INSTRUCCIONES DE USO</strong></p>
<p><strong><u>ALTA PRODUCTO</u></strong> <br />
  <br />
  <strong> Categoría y  Subcategoría; </strong>seleccionar donde  se visualizará el artículo<br />
  <strong>Código Almacén; </strong>Introducir (en  caso de necesidad) el código control del artículo según su criterio.<br />
  <strong>Código Fabricante;</strong> Introducir <strong>obligatoriamente</strong> el código que facilita el fabricante ya  que el artículo será visible en el sistema según dicho código<br />
  <strong>Marca;</strong> Nombre del  fabricante del artículo<br />
  <strong>Descripción;</strong> Descripción del  artículo<br />
  <strong>Link;</strong> Dirección  referente a la información sobre el artículo del cual el sistema  se basará para visualizar dicha Web al  profesional<br />
  <strong>Link Ofertas;</strong> Dirección donde  el profesional visualizará sus ofertas que estén fuera de la aplicación<br />
  <strong>PVP;</strong> Introducir precio  venta publico según catálogo<br />
  <strong>Descuento una Ud.</strong>; Introducir porcentaje de descuento una Ud.<br />
  <strong>Uds. máxima de descuento; </strong>Introducir cantidad máxima a ofertar<br />
<strong>Descuento máximo;</strong> Introducir porcentaje de descuento referente a la cantidad máxima a  ofertar</p>
<p><strong><u>EDITAR PRODUCTO:</u></strong><strong> </strong><br />
  Introduciendo código fabricante accederá a la opción <strong>modificar  artículo</strong></p>
<p><strong><u>LISTA PRODUCTOS:</u></strong><br />
  Visualización de todos sus productos, precios,  descuentos, etc. visibles en el sistema, <strong>pulsando</strong> el código fabricante  accede directamente a la opción <strong>modificar artículo</strong></p>
<p><strong><u>CONFIRMAR PEDIDO:</u></strong><br />
  Introduciendo el código compra que posee el profesional  visualizará la descripción de la compra, datos profesional y precio neto definitivo  según el sistema</p>
<p><strong><u>EDITAR PERFIL:</u></strong> <br />
  Opción para modificar datos fiscales</p>
<p><strong><u>EDITAR CALENDARIO FESTIVOS:</u></strong><strong> </strong><br />
  Marcar sus días festivos anuales para que el profesional  visualice los días de compra, evitando conflictos con el profesional</p>
