<p class="titulopetit">1. SU COMPROMISO</p>
<P>
La opción ACEPTACION PEDIDO solo es utilizada  en el caso de querer  confirmar  la compra de los articulos seleecionados, recibiendo un email con la descripción de su pedido, precio neto (igual o inferior al visualizado en zona <strong>TARIFA</strong>) y código compra, siendo este obligatorio facilitárselo a su proveedor antes de 48 horas, validando así su derecho al descuento. </P>
<p>Se dispone de 48  horas para facilitar el código compra al proveedor seleccionado, en caso de  incumplimiento podrá ser expulsado del sistema, existiendo la vía para anular  pedidos en <strong>PEDIDOS</strong> <strong>PENDIENTES</strong> antes de haber recibido dicho  código, evitando la penalización de expulsión.</p>
